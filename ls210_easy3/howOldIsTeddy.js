/**
 * Build a program that randomly generates Teddy's age, and logs it to the console.
 * Have the age be a random number between 20 and 200 (inclusive).
 */

function teddyAge() {
  console.log(`Teddy is ${Math.floor(Math.random() * 180) + 20} years old!`);
}

